# 二十八宿度数（简化版，每宿约12.86度）
MANSION_WIDTHS = [
    ("角", 12), ("亢", 12), ("氐", 12), ("房", 12),
    ("心", 6), ("尾", 18), ("箕", 10), ("斗", 26),
    ("牛", 8), ("女", 12), ("虚", 10), ("危", 17),
    ("室", 16), ("壁", 9), ("奎", 16), ("娄", 12),
    ("胃", 15), ("昴", 11), ("毕", 17), ("觜", 2),
    ("参", 11), ("井", 34), ("鬼", 3), ("柳", 15),
    ("星", 7), ("张", 17), ("翼", 18), ("轸", 17),
]


def map_to_mansion(degree: float) -> Tuple[str, float]:
    """
    将黄道度数映射到二十八宿
    
    Args:
        degree: 黄道度数（0-360）
    
    Returns:
        tuple: (宿名, 宿内度数)
    """
    degree = degree % 360
    
    cumulative = 0
    for name, width in MANSION_WIDTHS:
        if degree < cumulative + width:
            inner_degree = degree - cumulative
            return (name, inner_degree)
        cumulative += width
    
    # 兜底（理论上不会到这里）
    return ("角", degree)


def get_mansion_name(degree: float) -> str:
    """获取宿名"""
    return map_to_mansion(degree)[0]
